function suma(a, b) {
    return a + b;
}

function resta(a, b) {
    return a - b;
}

function multiplicacion(a, b) {
    return a * b;
}

function division(a, b) {
    return a / b;
}
console.log("Suma: 4 + 5 =", suma(4, 5));
console.log("Resta: 3 - 6 =", resta(3, 6));
console.log("Multiplicación: 2 * 7 =", multiplicacion(2, 7));
console.log("División: 20 / 4 =", division(20, 4));